    private void applyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyBtnActionPerformed
        // TODO add your handling code here:
        String coupon = couponJTF.getText();
        
        if(coupon.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please enter coupon code!");
        }
        else{
            try{
                dbc = new DBConnect();
                dbc.connectToDB();
                ResultSet rs = dbc.queryToDB("select * from Coupon where code = '"+coupon+"' and validity > GETDATE()");
                
                if(!rs.next())
                {
                    JOptionPane.showMessageDialog(null, "Coupon code is NOT valid!");
                    coupDisc = 0;
                }
                else
                {
                    per = rs.getDouble("percentage");
                    max = rs.getInt("max_amount");
                    JOptionPane.showMessageDialog(null, "Coupon added succesfully ");
                    showSub();
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);            
            }
        }
        
    }//GEN-LAST:event_applyBtnActionPerformed

