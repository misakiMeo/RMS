    private void showSub()
    {
        subLabel.setText(String.valueOf(subTotal));
        vat = subTotal * 0.075;            
        coupDisc = subTotal * (per/100);
        if(coupDisc>max)
        {
            coupDisc = max;
        }
        vatLabel.setText(String.valueOf(vat));
        couDisLabel.setText(String.valueOf(coupDisc));
        poiDisLabel.setText(String.valueOf(pointDisc));
        total = subTotal + vat - coupDisc - pointDisc;
        totalLabel.setText(String.valueOf(total));
    }
    
