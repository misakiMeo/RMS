    public void cons()
    {
        jLabel1.setText(uid);
        ReservationPan rp = new ReservationPan(currentEmp);
        InvoicePan ip = new InvoicePan(currentEmp);
        CustomerPan cp = new CustomerPan();
        panelSlide1.setAnimate(100);
        panelSlide1.init(rp,ip,cp);
        
        rp.addEventInvoice(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                //  Show register form
                panelSlide1.show(1);
                
            }
        });
        
        ip.addEventReservation(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                //  Show register form
                panelSlide1.show(0);
                
            }
        });
        
        ip.addEventCustomer(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                //  Show register form
                panelSlide1.show(2);
                
            }
        });
        
        cp.addEventBack(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                //  Show register form
                panelSlide1.show(1);
                
            }
        });
    }

