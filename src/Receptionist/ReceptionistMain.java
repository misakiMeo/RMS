    private int getEmployee() throws Exception{        
        dbc = new DBConnect();
        dbc.connectToDB();
        rs = dbc.queryToDB("select top 1 employee_id from LoginCredentials where uname='"+uid+"'");
        rs.next();
        return rs.getInt("employee_id");
    }
