    public void showMenuTable()
    {
        try{
            dbc = new DBConnect();
            dbc.connectToDB();
            DefaultTableModel model = (DefaultTableModel) MenuTable.getModel();
            model.setRowCount(0);
            rs = dbc.queryToDB("select * from Coupon");
            
            while(rs.next())
            {
                String up_id = String.valueOf(rs.getInt("coupon_id"));
                String up_code = rs.getString("code");
                String up_per = String.valueOf(rs.getFloat("percentage"));
                String up_date = String.valueOf(rs.getDate("validity"));
                String max = String.valueOf(rs.getInt("max_amount"));

                String TableData[] = {up_id, up_code, up_per, max, up_date};
                DefaultTableModel Tmodel = (DefaultTableModel) MenuTable.getModel();
                Tmodel.addRow(TableData);
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            
        }
    }
    
