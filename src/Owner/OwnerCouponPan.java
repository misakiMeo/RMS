    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        String add_code = addCode.getText();
        float add_per = Float.valueOf(addPer.getText());
        int add_max = Integer.valueOf(addMax.getText());
        long millis = addExpiry.getDate().getTime();
        String add_expiry = new java.sql.Date(millis).toString();
        
        if(add_code.isEmpty() || add_expiry.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "All fields are required!");
        }
        
        else{
            try{
                dbc = new DBConnect();
                dbc.connectToDB();
                boolean qr = dbc.ToDB("insert into Coupon"
                                       +" values ('"+add_code+"',"+add_per+", '"+add_expiry+"', "+add_max+")");

                if(qr)
                {
                    JOptionPane.showMessageDialog(null, "Coupon Added Successfully");
                    showMenuTable();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Error adding coupon");
                }
            }catch(Exception e){

            }
        }

        
    }//GEN-LAST:event_addBtnActionPerformed

    private void searchItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchItemActionPerformed
        // TODO add your handling code here:

        iid = searchBox.getText();
        try{
            dbc = new DBConnect();
            dbc.connectToDB();

            if( !iid.isEmpty())
            {
                rs = dbc.queryToDB("select top 1 * from Coupon where code like '%"+iid+"%'");
            }
           
            else
            {
                JOptionPane.showMessageDialog(null, "Please enter code");
            }
            
            if(!rs.next())
            {
                JOptionPane.showMessageDialog(null, "No coupon code found!");
                delCode.setText("");
                delPer.setText("");
                delExpiry.setText("");
                delMax.setText("");
                upCode.setText("");
                upPer.setText("");
                //upExpiry.setText("");
                upMax.setText("");
            }
            else
            {
                String up_code = rs.getString("code");
                float up_per = rs.getFloat("percentage");
                String up_date = String.valueOf(rs.getDate("validity"));
                int max = rs.getInt("max_amount");
                
                upCode.setText(up_code);
                upPer.setText(String.valueOf(up_per));
                upExpiry.setDate(java.sql.Date.valueOf(up_date));
                upMax.setText(String.valueOf(max));
                
                delCode.setText(up_code);
                delPer.setText(String.valueOf(up_per));
                delExpiry.setText(up_date);
                delMax.setText(String.valueOf(max));

            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);

        }
    }//GEN-LAST:event_searchItemActionPerformed

